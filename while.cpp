#include<iostream>
using namespace std;

int main(){

    int i=1;
    while(i<10){
        /* loop will iterate untill condition reaches 10. So, when condition
        reaches 10, the execution of loop will be terminated. That's why this
        code will print upto 9.*/
        cout << i << " ";
        i++;
    }

    return 0;
}
